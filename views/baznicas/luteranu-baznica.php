<h3>Par draudzi</h3>
<p>Sākotnēji Kolkas draudze bija Irbes, tagadējās Mazirbes, filiāldraudze. Patstāvīga Kolkas evaņģēliski luteriskā draudze izveidojās 1919. gadā. Otrais pasaules karš atnāca arī ar pārtraukumu draudzes darbībā. Kaut arī pēckara draudzes darbība tika atjaunota, tā nespēja samaksāt valsts noteiktās nodevas un dievnams draudzei tika atsavināts. Savukārt, 1969. gadā tika izbeigta arī draudzes pastāvēšana.<br>Pēc 20 gadiem, 1989. gadā, draudze atsāka savu darbību. Atjaunotajā draudzē bija ap 80 draudzes locekļu. Dievnamā sākas evaņģēlija pasludināšanas darbs — dievkalpojumi, kristības, iesvētīšana un laulību dievkalpojumi. Būtisku ieguldījumu draudzes un dievnama atjaunošanā devusi ilggadējā draudzes priekšniece Dzidra Bērziņa (1989. – 2006.). Vēlāk par draudzes priekšnieci kļūst Olita Kalna (2006. – 2012.). Kopš 2012. gada draudzes priekšniece ir Vija Ernštreite.</p>
<p>Līdz ar draudzes atjaunošanas tai ir izveidojusies laba sadarbība ar māsu draudzi Savitaipalē (Somijā), kura ir uzdāvinājusi Kolkas dievnamam elektriskās ērģeles.</p>
<p>Zīmīgi, ka pirmās Latvijas brīvvalsts laikā Kolkas draudzē ir kalpojis&nbsp; somu mācītājs H. K. Ervie, kurš ir sprediķojis līvu valodā. Savukārt, atjaunotajā draudzē ir kalpojuši sekojoši garīdznieki: mācītājs Vilnis Jansons (1990. — 1998.), mācītājs Raitis Šēners (1998. – 2002.), mācītājs Māris Sarma (2002. – 2005.), mācītājs Āris Kronbergs (2005. – 2009.), evaņģēlists Ainārs Kostanda (2009. – 2013.), mācītājs Ilmārs Barons (2013. — 2015.). Šobrīd kalpošanu turpina mācītājs Kārlis Irbe (no&nbsp;2013.).</p>
<p>Šobrīd draudzē ir ap 40 draudzes locekļu. Draudze ik svētdienu pulcējas dievkalpojumos, kā arī regulāri notiek Bībeles studijas un Svētdienas skola bērniem. Draudzes locekļi ir aktīvās atpūtas cienītāji, dodoties gan tuvākās, gan tālākās ekskursijās. Kā arī līdz ar citām Kolkas draudzēm ņem līdzdalību ekumēniskos pasākumos Kolkā. Lai būtu iespējams izbaudīt Kolkas dievnama labo akustiku draudze labprāt organizē garīgās mūzikas koncertus gan baznīcas svētkos, gan Kolkas ciema svētku pasākumu ietvaros.</p>

<hr>
<h3>Par dievnamu</h3>
<p>Pirmā Kolkas dievnama uzcelšanas laiks un vieta nav zināma. Arhīvos ir atrodamas ziņas par mazu koka dievnamu, kas uzcelts 1833. gadā, tas varētu būt otrais vai trešais dievnams Kolkā.</p>
<p>Vēl 20. gadsimta pirmajā pusē Kolkas dievnamā atradušās divas, iespējams pirmā dievnama altārgleznas, kas datētas ar 1648. gadu. Abas altārgleznas – «Krustā sistie»&nbsp;un «Kristus augšamcelšanās»&nbsp;gleznotas ar eļļas krāsām uz apses koka dēļiem. «Kristus augšāmcelšanās»&nbsp;ir saglabājusies un atrodas Rundāles pils muzejā.</p>
<img alt="" class="alignright size-medium wp-image-10183" src="../../content/bazn2-220x330.jpg" align="right" width="300" style="padding-left:25px" title="Kolkas evaņģēliski luteriskā baznīca">
<p>Pēc Krimas kara dievnams bija tik ļoti cietis, ka vairs nebija izmantojams, tādēļ toreizējais Dundagas muižas īpašnieks barons Kārlis Ludvigs Ferdinands Ostens-Zakens lika uzcelt jaunu dievnamu. 1886. gada otrajā adventē dievnams tika iesvētīts un nodots Kolkas luterāņu draudzei. Dievnamā bija kokgrebuma krucifikss. Pateicoties gan pašu kolcinieku, gan kaimiņu draudžu ziedojumiem, 1936. gadā tika iesvētītas Kolkas dievnama pirmās ērģeles.</p>
<p>Otro pasaules karu dievnams pārdzīvoja bez postījumiem, tomēr padomju gados, kad dievnamu pārņēma Kolkas ciema padome un nodeva to zivju konservu fabrikas rīcībā, dievnams tika izlaupīts un izpostīts. Vēlāk dievnamā tika ierīkota makulatūras noliktava, tā turpinājās līdz 1989.&nbsp;gadam.</p>
<p>1991. gadā sākās vērienīga dievnama atjaunošana, kas vainagojās ar atjaunotā Kolkas evaņģēliski luteriskā dievnama iesvētīšanu 1993.&nbsp;gadā.</p>
<p>Dievnama pagalmā ir iespēja apskatīt daļu no 1880. gadu beigās izveidotā laukakmeņu žoga. Dievkalpojumi tiek iezvanīti ar 1926. gadā iesvētīto dievnama zvanu, ko izdevās atgūt dievnama atjaunošanas laikā.</p>
<p>Kolkas dievnams ir arī sava leģenda – par «asins akmeni», bet to jūs uzzināsiet apciemojot mūs.</p>
