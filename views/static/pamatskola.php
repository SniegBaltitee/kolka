															<h3>Kolkas pamatskolas vēsture</h3>
                                                            <p>Kolkas pamatskolas jaunā ēka ir celta 1961. gadā. Kopš 1994. gada Kolkas pamatskola ir Kolkas pagasta padomes pakļautībā, no 2009. gada līdz 2021.gadam Dundagas novada iestāde, bet 2021. gadā, izveidojoties Talsu novadam, Kolkas pamatskola ir Talsu novada pašvaldības iestāde ar divām ēkām adresēs «Kolkas pamatskola»  un «Bērnudārzs «Rūķītis»».<br>Skola nodrošina arī pirmsskolas izglītības programmu «Bērnudārza «Rūķītis»» ēkā. Ēkas otrajā stāvā izvietojies internāts «Rūķu nams» ar 15 gultas vietām. Internāta pakalpojums pieejams arī pirmsskolas bērniem no 4 gadu vecuma.</p>
