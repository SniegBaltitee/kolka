                                                            <p>Kolkas pamatskola ir savā domāšanā un pārliecībā zaļa, priežu mežu un jūras viļņu ieskauta EKOskola, kuras moto:</p>
                                                            <center><p>Pats domā, spried un sver,<br>Pats esi kungs,<br>Pats savai laimei durvis ver.<br>/Rainis/  </p></center>
