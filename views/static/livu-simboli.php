															<h3>Karogs un karoga svētki</h3>
                                                            <div>
                                                            <p><img alt="Lībiešu karogs" class="alignright" src="../../content/img_0465.jpg" style="margin-left: 8px; margin-right: 8px; margin-top: 8px; margin-bottom: 8px; float: right; width: 250px; height: 308px; " title="Lībiešu karogs"><strong>Lībiešu karogu</strong>&nbsp;saskaņā ar Līvu savienības ieteikumu izgatavoja Tērbatā. Krāsu salikums karogā bija zaļš, balts, zils. Ir saglabājušās Līvu savienības aktīvista&nbsp;<strong>Didriķa Volganska</strong>&nbsp;(Valguma) lībiski rakstītās atmiņas, kurās viņš stāsta, ka toreiz lībieši karogam esot izvēlējušies krāsas, kas cieši saistītas ar lībiešu zvejnieku darbavietu – jūru. Braucot malā no zvejas zilajā jūrā zvejnieks vispirms redz zaļo mežu, tad smilšu kāpas. Tāpēc karogs veidojams zaļš, balts, zils. Igauņi domā, ka ideju izmantot korporācijas&nbsp;<em>Curonia</em>&nbsp;krāsas — zaļš, zils, balts — varētu būt piedāvājuši Tērbatas universitātes studenti, kas lībiešus apmeklēja valodas ekspedīcijās. Alegoriski to varot saistīt arī ar lībiešu nacionālās atdzimšanas ideju. Lībiešu karoga baltajai svītrai būtu jāsimbolizē lībiešu kultūras autonomija. D. Volganskis atceras, ka karogu bija sagādājusi Igaunijas Dzimtās valodas biedrība un lībiešiem to uzdāvinājis tās pārstāvis.</p>
                                                            <p>Karogu iesvētīja un pirmoreiz uzvilka svētkos 1923. gada 18. novembrī Irbes mācītājmuižā. Pēc pasniegšanas līdz uzvilkšanas vietai to aiznesa Līvu savienības valdes loceklis&nbsp;<strong>Kārlis Stefenbergs</strong>. Līvu savienības priekšsēdētājs, lībiešu dzejnieks&nbsp;<strong>Kārlis Stalte</strong>&nbsp;teica runu un pēc tam visi nodziedāja «Dievs Kungs mūsu stiprā pils». Koris nodziedāja arī K. Staltes šim gadījumam speciāli sacerēto&nbsp;<em>Plagā loul</em>&nbsp;(Karoga dziesma) un vēl citas lībiešu dziesmas.</p>
                                                            <p>Laikraksta&nbsp;<em>Līvli</em>&nbsp;1999. gada 2. numurā ir publicēts soma Marti Hāvio (1899 – 1973) raksts «Lībiešu karoga svētki». Marto Hāvio pats bija piedalījies 1923. gada 18. novembra svētkos Mazirbē un savus iespaidus tolaik aprakstījis žurnālā&nbsp;<em>Suomen Heimo</em>&nbsp;1924. gada 1. numurā. Viņš rakstīja:</p>
                                                            <p>&nbsp;</p>
                                                            <blockquote>
                                                            <p style="padding-left: 30px;"><span style="color: #008000;"><em>18. novembris pārvērtās par ievērības cienīgu dienu lībiešu vēsturē. Šo dienu Kurzemes piekrastes saimnieki paziņoja par īpaši tautisku. Šajā dienā lībiešu ģimenes pielika pie sienas lepnus, aizkustinošus un pašpārliecinātus vārdus: Būsim tādi kā mūsu tēvi: lībieši kā akmeņi cieti! Šajā dienā svētdienīgi ģērbtā tauta asarām acīs pirmo reizi savā valodā dziedāja visiem luterāņiem svēto baznīcas dziesmu «Dievs Kungs ir mūsu stiprā pils».</em></span></p>
                                                            </blockquote>
                                                            <p>&nbsp;</p>
                                                            <hr>
                                                            <h3>Lībiešu karoga svētki 2003. gada 18. novembrī</h3>
                                                            <p>Lai nosvinētu lībiešu karoga astoņdesmitgadi, lībieši 2003. gada 18. novembrī pulcējās Mazirbē. Līvu savienības organizētos karoga svētkus ievadīja Latvijas neatkarības 85 gadu jubilejai un lībiešu karoga svētkiem veltītais dievkalpojums Mazirbes baznīcā. Uz dievkalpojumu bija pulcējušies lībieši, viņu pēcnācēji un interesenti no Mazirbes, Kolkas, citiem lībiešu piekrastes ciemiem, no Ventspils un Rīgas. Tāpat kā tālajā 1923. gada 18. novembrī baznīcā atkal skanēja korālis «Dievs Kungs mūsu stiprā pils». Dievkalpojuma norisi lieliski papildināja Rīgas lībiešu dziesmu ansambļa&nbsp;<em>Līvlist</em>&nbsp;lībiešu valodā dziedātās dziesmas.</p>
                                                            <p>Pēc dievkalpojuma svētku dalībnieki kārtojās gājienam uz Tautas namu. Lībiešu karogiem lepni plīvojot, gājiens devās cauri Mazirbei, kur pie vienas otras mājas blakus Latvijas valsts karogam bija redzams arī lībiešu karogs.<img alt="Lībiešu karoga svētkos Mazirbē 2003. gada 18. novembrī. Svētku dalībnieku..." src="content/karogi_2003.jpg" style="margin-left: 8px; margin-right: 8px; margin-top: 8px; margin-bottom: 8px; width: 600px; height: 393px; " title="Lībiešu karoga svētkos Mazirbē 2003. gada 18. novembrī. Svētku dalībnieku...">Tautas namā kopīgi tika nodziedāta Latvijas un lībiešu himna. Šķiet, ka pirmo reizi Latvijas himna «Dievs svētī, Latviju» tika nodziedāta arī lībiešu valodā. Līvu Savienība sadarbībā ar valsts īpaši aizsargājamās lībiešu kultūrvēsturiskās teritorijas «Lībiešu krasts» direkciju bija izdevusi nelielu lībiešu dziesmu brošūriņu&nbsp;<em>Plagā loul</em>&nbsp;(Karoga dziesma), tāpēc visi klātesošie dziesmas varēja dziedāt lībiski.</p>
                                                            <p>Svētku uzrunu teica Līvu savienības vecākais&nbsp;<strong>Aldis Ermanbriks</strong>, lībiete&nbsp;<strong>Valda Marija Šuvcāne</strong>&nbsp;pastāstīja par lībiešu karoga vēsturi. Līvu savienība 2003. gadā bija iegādājusies simts lībiešu nacionālo karogu un tie tika uzdāvināti <span class="caps">LS</span> ilggadējiem aktīvistiem. Oficiālās daļas noslēgumā A. Ermanbriks prezentēja tikko sagatavoto skaņu kaseti «Kāzas jūrā» ar lībiešu folkloras tekstu paraugiem, kurus ierunājusi V. M. Šuvcāne.</p>
                                                            <p>Svētku saviesīgajā daļā ar priekšnesumiem uzstājās lībiešu dziesmu ansambļi&nbsp;<em>Līvlist</em>&nbsp;un<em>Kāndla</em>, folkloras kopas&nbsp;<em>Skandinieki</em>&nbsp;pārstāvji, bija iespējams vērot videoierakstus par lībiešiem, varēja kopīgi padziedāt un aprunāties ar savējiem no visām lībiešu grupām.<br>Turpmākajos gados Līvu savienība 2003. gadā iedibināto tradīciju -&nbsp;<strong>lībiešu karoga svētku&nbsp;</strong>svinēšanu 18. novembrī turpināja.</p>
