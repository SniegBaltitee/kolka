                                                            <h3>Emblēma</h3>   
                                                            <p><img alt="Mākslinieka Andreja Šulca zīmētā lībiešu emblēma" class="alignleft" src="../../content/libiesu_emblema.jpg" style="margin-left: 8px; margin-right: 8px; margin-top: 8px; margin-bottom: 8px; float: left; width: 100px; height: 156px; " title="Mākslinieka Andreja Šulca zīmētā lībiešu emblēma"><strong>Lībiešu emblēma</strong>&nbsp;ar karoga krāsām radās vēlāk. Mākslas akadēmijas students lībietis&nbsp;<strong>Andrejs Šulcs</strong>&nbsp;emblēmu uzzīmēja, lai to publicētu uz lībiešu kalendāra vāka&nbsp;<em>Rāndalist Ājgarāntõz</em>&nbsp;1933. gadā. Mūsdienās šī emblēma ir pamatā Līvu savienības nozīmītei.</p><br><br><br>       <br><br><br>                                          
                                                            <h3>Himna</h3> 
                                                            <div style="text-align: right;">
<p style="text-align: justify;">Dziesmai&nbsp;<strong><em>Min izāmō, min sindimō</em></strong>&nbsp;(Mana tēvzeme, mana dzimtene), ko uzskatām par lībiešu himnu, ir tā pati somu un igauņu himnas melodija, ko komponējis.&nbsp;<strong>F. Paciuss</strong>, bet dziesmas vārdus sacerējis lībiešu dzejnieks&nbsp;<strong>Kārlis Stalte</strong>.</p>
<p style="text-align: justify;">Dziesma&nbsp;<em>Min izāmō, min sindimō</em>&nbsp;pirmo reizi skanējusi diezgan poētiskos apstākļos. Par to atmiņās stāsta somu valodnieks&nbsp;<strong>Lauri Ketunens</strong>. 1925. gada decembra beigās, ziemas brīvdienās, viņš aizbraucis uz Mazirbi un apmeties Kārļa Staltes mājās augšistabā. No rīta Stalte profesoru modinājis un aicinājis lejā. Kad Ketunens nokāpis, viņš ieraudzījis lībiešu kori, kurš Staltes vadībā, profesoru sveikdams, nodziedājis lībiešu valodā dziesmu «Mana tēvzeme, mana dzimtene» somu un igauņu himnas melodijā. Dziesmas vārdus Stalte bija sacerējis jau agrāk, tie publicēti 1924. gadā Kārļa Staltes dzejoļu grāmatiņā&nbsp;<em>Līvo lōlõd</em>.</p>
<p style="text-align: justify;">Ketunenam dziesma ļoti patikusi, viņš atzinis:</p>
<p>&nbsp;</p>
<blockquote>
<p style="padding-left: 30px;"><span style="color: #008000;"><em>Pēc manām domām tā dziesma ir vislabākā no tām trim, kuras izpaužas vienādi jaukā melodijā.</em></span></p>
</blockquote>
<p>&nbsp;</p>
<p style="text-align: justify;">Profesors uzskatīja, ka Stalte sacerējis šai himniskajai dziesmai lībiešu jūrmalai un dzīvei ļoti piemērotus vārdus. To varēja dziedāt saietos, kuros piedalījās somi un igauņi — katrs ar saviem vārdiem. Nošu krājumā&nbsp;<em>Līvõkīel lōlõd</em>, kas ar Ketunena gādību iznāca Helsinkos 1929. gadā, tā ievietota kā&nbsp;pirmā.</p>
