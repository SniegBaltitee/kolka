															<h4>Mākslinieciskās pašdarbības kolektīvi patlaban</h4>
															<img src="../../content/111.jpg" width="250" height="400">
															<p>2000. gadā ar toreizējās tautas nama direktores Ritas Leitendorfas iniciatīvu tika izveidots līvu dziesmu ansamblis «Laula» (nedaudz latviskots lībiešu vārds, tulkojumā – dziesma). To sākumā vadīja Ilona Jostiņa, vēlāk vadību pārņēma Dzintra Tauniņa. Kolektīvs piedalījies ansambļu skatēs, Lībiešu svētkos Mazirbē, folkloras festivālā Slovākijā, regulāri priecē klausītājus dažādos pasākumos Dundagas novadā.Dzintra Tauniņa vada arī Kolkas tautas nama un Dundagas mākslas un mūzikas skolas koklētāju ansambļus, kuri jau daudzus gadus ir neiztrūkstoši Dziesmu un deju svētku dalībnieki.</p>
