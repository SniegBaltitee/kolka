															<p>Gadu gaitā jaunuzceltais klubs piedzīvojis daudzas pārmaiņas. 1979. gadā tiek nojauktas krāsnis un ierīkota centrālapkure. Kopš 1989. gada kluba sienas grezno skaisti gobelēni ar lībiešu motīviem. Tos kā diplomdarbu veidojuši Mākslas akadēmijas studenti Antra Auziņa un Oskars Degro. Klubs tiek pārdēvēts par TAUTAS NAMU, kas ir šī nelielā Kolkas ciema kultūras centrs. Kopš 1999. gada tautas nama otrajā stāvā darbojas Kolkas mūzikas skola. 2003 – 2004. gadā atjaunota kluba fasāde, izremontēta mazā zāle, priekštelpa, telpas aiz skatuves. 2010. gadā iegādāti jauni zāles krēsli un gūts atbalsts ELFLA (Eiropas Lauksaimniecības fonds lauku attīstībai) projektam «Kolkas tautas nama vienkāršotā rekonstrukcija», kura realizācijas rezultātā 2011. gadā Kolkas tautas nams iegūst jaunu jumta segumu, jaunus logus, rekonstruētu apkures sistēmu, lielās zāles remontu u.c. uzlabojumus.Lai nodrošinātu kvalitatīvu pasākumu tehnisko pusi, pastāvīgi tiek atjaunots un uzlabots tehniskais nodrošinājums– ar Kolkas tautas nama aprīkojumu var nodrošināt gan iekštelpu, gan ārtelpu plaša spektra un apmēra pasākumus.</p>
