															<p>Kolka gan mūsdienās, gan tālākā pagātnē var lepoties ar bagātu kultūras dzīvi. Kamēr Kolkā vēl nebija tautas nama, visi ciema kultūras pasākumi notikuši vecajā skolā, kas pēc Pirmā pasaules kara darbojās tagadējos «Pastniekos». Kolkas zvejnieku biedrība «Rīts» 1922. gadā pieprasīja savā īpašumā uz valsts fonda zemes esošo bijušo krievu robežsargu <strong>kordonu</strong> (cara laikā celtu ēku krievu zaldātiem). Tā bija neliela mūra māja, kurā bija ierīkotas kazarmas. Turpat netālu atradās arī mūra staļļi zirgiem. 1929. gadā zvejnieku biedrība izremontēja ēku un ierīkoja zāli ar skatuvi. Latvijas pirmās brīvvalsts laikā šeit atradās <strong>Kolkas biedrības nams</strong>. Ieeja biedrības namā atradās ēkas sānā, bet otra ieeja – jūras pusē. Skatuve bija tajā pusē, kur šobrīd ir tautas nama galvenā ieeja. Kolkas biedrības namā notika dažādi sarīkojumi, tējas vakari, balles. Aktīvi darbojās dāmu komiteja. Tā rīkoja rokdarbu izstādes, organizēja interesantus pasākumus ar Laimes akām, atrakcijām, dziedāja, spēlēja teātri. Ciema sabiedrisko dzīvi vadīja skolotājs <strong>Kārlis Bernšteins</strong></p>
															<p>Arī kara laikā biedrības nams turpināja darboties. 1944. gadā vietējiem iedzīvotājiem ciems bija jāatstāj. Palika tikai nedaudz cilvēku un krasta apsardze. Tūlīt pēc kara piecdesmitajos gados tika sakārtotas bijušā biedrības nama telpas, ko tad jau sauca par KLUBU. Tolaik atsākās spraigs jauncelsmes darbs, jo ciemā ieradās ļoti enerģisks, neatlaidīgs cilvēks, kurš organizēja zivju apstrādes fabrikas celtniecību. Šis cilvēks bija <strong>Jevgēnijs Morozovs</strong>. Ciemā ieradās daudz cilvēku, kuri piedalījās celtniecības darbos.</p>
															<hr>
															<img src="../../content/klubs_jauc.jpg" width="300" height="600">
															<p>Atsākās pašdarbība, izveidojās tautas deju ansamblis, spēlēja teātri, rādīja filmas. 1953. gadā nodibinājās kopīgs Kolkas – Mazirbes jauktais koris, kurā dziedāja 35–40 sievas un apmēram 30 vīri.. Tā kā kluba telpas bija ļoti šauras, tad mēģinājumi notika zivju fabrikas laboratorijā, balles tika rīkotas sālītavas telpās vai arī virs tagadējās mehāniskās darbnīcas, kur bija plašas noliktavas. Tur notika interesanti karnevāli, saviesīgi vakari. Balles, kurās spēlēja Kolkas orķestris (akordeons, vijole, mandolīna, bungas), parasti beidzās tikai rīta pusē. Daudzi pasākumi tolaik notika arī «Pastniekos». Aktīvā sabiedriskā darbība mudināja zivju fabrikas direktoru J. Morozovu risināt plašāku telpu jautājumu. Radās ideja par jauna kluba celtniecību, taču nebija naudas. Sākot ar 1954. gadu tika regulāri organizētas komjauniešu, jauniešu talkas, kurās sāka nojaukt veco ēku jūrmalā. Šajās talkās piedalījās ļoti daudz cilvēki – gan veci, gan jauni, pat bērni. Parasti talcinieku barā atradās kāds muzikants, kurš vēlāk sāka spēlēt. Tad sākās dziedāšana un dejošana visu nakti, bet no rīta katrs bija savā darba vietā. Apmēram divu gadu laikā tika paveikts milzīgs darbs – nojaukta iepriekšējā, vecā ēka tā, ka palika tikai pamati un viena siena. Jaucot ēku, tās mūros atrada iemūrētas cara laika monētas.</p>
															<p>Vecie pamati tika paplašināti, sākās jauna kluba celtniecība, kuru vadīja Volbedahts. Celtnieki no Valdemārpils piebūvēja klāt zāles telpu, telpas aiz skatuves, uzbūvēja otro stāvu. Lai celtniekiem apmaksātu darbu, tika rakstīti fiktīvi akti par kapitālo remontu, taču patiesībā tika celta pilnīgi jauna ēka. Par šādu neatļautu darbību tika rakstītas sūdzības Finansu inspekcijai, pat Finansu ministrijai. Neskatoties uz to, ēku cēla ar vērienu – tika likti ozolkoka paneļi, parkets.Tautas nama griestu dekors tautiskā-padomju stilā.Griestu rotājumu skices veidoja Haralds Rubenis, taču viņa iecerētos tautiskos ornamentus neatļāva izgatavot. Tos nācās pārstrādāt, lai attēlotu tā laika raksturīgāko simboliku – sirpi un āmuru, kuru varam redzēt vēl šodien. Kad ieradās pārbaude no Finansu ministrijas, kluba ēka jau bija gatava. Komisija apskatīja jauno celtni, konstatēja faktu, taču neko vairs nevarēja darīt. Par nelikumīgo darbību zivju fabrikas direktors J. Morozovs saņēma tikai rājienu, kaut pienācās atbrīvojums no darba. Viņu aizstāvēja Zivrūpniecības ministrs Kārlis Bude, kurš bija labs direktora draugs.</p>	
															<img src="../../content/dekors.jpg" width="250" height="400">
															<hr>
															<p><strong>Klubu atklāja 1955.gada Ziemassvētkos.</strong> Todien bija ļoti liels putenis. Uz svinībām aicinātais Zivrūpniecības ministrs K. Bude, kurš brauca no Ventspils, līdz klubam netika, jo viņa mašīna iegrima kupenā pie Kolkas baznīcas. Tā atklāšanas svinības sākās ar nokavēšanos, jo vajadzēja palīdzēt ministram izkļūt no klizmas. Zālē pirmajās rindās sēdēja kluba celtnieki un pašdarbnieki, tērpušies pašu gatavotos tērpos. Zāle bija cilvēku pilna. Tas visiem bija priecīgs un gandarījuma pilns brīdis. Vēlāk sākās balle, kura turpinājās līdz pat rītam. Laikraksta «Padomju Dundaga» 1955. gada 28. decembra numurā A. Hartmaņa rakstā „Svētki zvejniekciemā” teikts, ka ar jaunā kluba atklāšanu Kolkas zvejniekiem un strādniekiem izveidojies lielisks atpūtas un kultūras centrs. Tajā ir zāle ar 360 vietām, iekārtota skatuve, telpas bibliotēkai un mākslinieciskās pašdarbības pulciņiem. Ēkas otrajā stāvā uzstādīta stacionāra kino iekārta.</p>
															<img src="../../content/atklasana.jpg" width="500" height="800">
															<hr>
															<p>Jaunais kultūras nams tobrīd bija viens no labākajiem apkārtnes klubiem. Kultūras dzīve Kolkā kļuva vēl intensīvāka.Uz Kolku viesizrādēs brauca gan profesionālie, gan tautas un amatierteātri no Rīgas, Liepājas, Valmieras, Ventspils, Talsiem, Jelgavas, Ogres. Ar koncertiem Kolkā viesojās tādi visā republikā pazīstami dziedātāji kā Edgars Plūksna, Pēteris Grāvelis, Ārija Simsone, Edgars Zveja, komponisti – Marģers Zariņš, Raimonds Pauls un citi mākslinieki, kas atzinuši, ka Kolkas zālē ir laba akustika. Filmas veda no Talsiem, kino rādīja divas vai trīs reizes nedēļā, zāle vienmēr pilna skatītāju. Tolaik arī cilvēki bija ļoti atsaucīgi, aktīvi, darbīgi. Notika interesanti karnevāli ar asprātīgi veidotām maskām. Mākslinieki – noformētāji vienmēr gādāja, lai telpas būtu skaisti izdekorētas.</p>
