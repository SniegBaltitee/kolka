															<h3>Līvu savienības Kolkas grupa un lībiešu centrs Kūolka</h3>
                                                            <p>Lībieši savu savienību atjaunoja 1988. gada 26. novembrī un nosauca to par <strong>Līvu&nbsp;kultūras&nbsp;savienību</strong>&nbsp;(<em>Līvõd kultūr īt</em>), jo toreiz Latvijas minoritātēm atļāva organizēties tikai šādās kultūras biedrībās. Atjaunotajā savienībā iestājās 139 cilvēki. Savu agrāko nosaukumu&nbsp;<strong>Līvu&nbsp;savienība</strong>&nbsp;(<em>Līvõd Īt</em>) atjaunoja 1993. gada 20. februārī. Veidoja arī reģionālās grupas. Ventspils grupu noorganizēja 1989.gada 18. februārī, bet Kolkas grupu — 1989. gada 18. jūlijā.</p>
                                                            <p>Laikā, kad valstī bija sākušās cerīgas pārmaiņas, bija svarīgi noskaidrot, cik lībiešu vēl prot savu dzimto valodu un var tajā brīvi sarunāties. Šādu sarakstu 1980. gadu vidū izveidoja lībiešu pētniece&nbsp;<strong>Valda Marija Šuvcāne</strong>. Tobrīd Kolkā valodu brīvi pārvaldīja vēl 13 lībiešu. Iespējams, ka saraksts nebija pilnīgs, taču galveno tas parādīja — dzimtās valodas runātāju bija gauži maz, un tie visi bija cilvēki krietnos gados (miršanas gadi pievienoti vēlāk).</p>    
