                                                            <p>1989. gadā notika Vissavienības tautas skaitīšana. Sadaļā «Talsu rajona Kolkas pagasta pastāvīgo iedzīvotāju nacionālais sastāvs» redzams, ka pagastā dzīvoja 37 lībieši, no tiem lībiešu valodu par dzimto nosaukuši 15 cilvēku, bet pārējie uzrādījuši, ka viņu dzimtā valoda ir latviešu.</p>
                                                            <hr>
                                                            <p>Par&nbsp;<strong>Līvu savienības Kolkas grupas dibināšanu</strong>&nbsp;1989. gada 18. jūlijā stāsta sapulces protokols. Pulksten 10.00 notika sapulce Kolkas klubā, lai iedzīvotājus iesaistītu Lībiešu kultūras savienībā. Sapulci vadīja <span class="caps">LS</span> valdes locekle un grāmatvede, bijusī kolceniece,<strong>Emīlija Rozenfelde</strong>. Ieradās 38 cilvēki, no kuriem 24 iesniedza pieteikumus un tika uzņemti par savienības biedriem. Par priekšsēdētāju vienbalsīgi ievēlēja ciema kluba vadītāju&nbsp;<strong>Dinu Freimani</strong>. Galvenie uzdevumi bija tādi paši kā <span class="caps">LS</span> statūtos: uzturēt dzīvu lībiešu valodu, lībiešu vidū izplatīt zinības un mācības, palīdzēt uzlabot lībiešu ekonomisko un sociālo dzīvi.</p>
                                                            <p>1990. gada 9. novembrī notika Kolkas grupas biedru kopsapulce. To vadīja <span class="caps">LS</span> valdes pārstāve&nbsp;<strong>V. M. Šuvcāne</strong>. Par Kolkas grupas jauno priekšsēdētāju ievēlēja&nbsp;<strong>Gundaru Bertholdu</strong>, par vietnieci —&nbsp;<strong>Mārīti Zandbergu</strong>, kasieri —&nbsp;<strong>Elzu Ešentāli</strong>.</p>
                                                            <p>No tā laika pagājuši jau divdesmit gadi. 2009. gada 18. jūlijā Kolkas grupa atzīmēja savas darbības nozīmīgo jubileju. Kas tad ir padarīts šais divdesmit gados? Viens no Kolkas grupas galvenajiem uzdevumiem vienmēr bijis lībiskā kultūrmantojuma izpēte un saglabāšana dzimtajā ciemā. Tāpēc tika organizēti tematiski pasākumi, pārgājieni, ekskursijas.</p>
                                                            <hr>
                                                            <p><img alt="Līvu savienības Kolkas grupas biedri 1994. gadā." class="alignright" src="../../content/grupa_1994.jpg" style="margin-left: 8px; margin-right: 8px; margin-top: 8px; margin-bottom: 8px; float: right; width: 300px; height: 196px; " title="Līvu savienības Kolkas grupas biedri 1994. gadā.">Grupas sekmīgai darbībai vajadzēja arī telpas. Ar toreizējās Kolkas pagasta padomes priekšsēdētājas&nbsp;<strong>Maijas Rērihas </strong>atbalstu Kolkas lībiešu grupai ierādīja telpas Kolkas ciema Zītaros. Bija iecerēts izveidot darba telpu, kura vienlaikus kalpotu arī par izstāžu zāli. Lai iekārtotu telpas, ciemā tika vākti materiāli par lībiešu tēmu. Daudz lībiešu sadzīves priekšmeti no Kolkas un apkārtējiem ciemiem 1950.–1960. gados bija aizceļojuši uz Igauniju, bet ievērojama daļa gājuši bojā Otrā pasaules kara laikā. Tomēr vēl tika sameklēti dažādi lībiešu darinājumi — rokdarbi, adījumi, sadzīves priekšmeti, zvejas piederumi, kokizstrādājumi. Tas bija pietiekami, lai varētu izveidot pirmo izstādi. Ar lielu entuziasmu Kolkas grupas biedri remontēja iegūtās telpas, iekārtoja un organizēja izstādi.</p>
                                                            <p>1993. gada 3. aprīlī atklāja ciema ļaužu sarūpēto izstādi un pašu spēkiem izremontēto un iekārtoto telpu, kuru toreiz nosauca par Lībiešu informācijas centru. Atklāšanā piedalījās arī dzejnieki Uldis Bērziņš un Juris Kronbergs no Zviedrijas. Daļu no izstādē eksponētajiem materiāliem cilvēki pavisam atdeva Lībiešu informācijas centra rīcībā. Tāds bija sākums. Laika ritumā papildinājies eksponātu skaits. Vērtīga ir&nbsp;<strong>Dzidras Bērziņas</strong>&nbsp;dāvātā pūralāde (1783. gads) un&nbsp;<strong>Arnolda Einberga</strong>&nbsp;garie ādas zvejnieku zābaki. Eksponātu skaits, kas priecē tūristus un pašmāju ļaudis, jau sniedzas otrajā simtā.</p>
                                                            <p>Kolkas lībiešu grupas darbībai aktivizējoties, sākumā ierādītās telpas kļuva par šaurām. 1999. gadā centram blakus atbrīvojās vēl divas istabas, un Kolkas pagasta padome, atbalstot lībiešu lūgumu, tās piešķīra informācijas centra vajadzībām. Lībiešu centra telpas paplašinājās līdz četrām istabiņām. 1999. gadā grupas valde centru nosauca par <strong>Līvu&nbsp;informācijas centru Kūolka</strong>.</p>
                                                            <p>2000. gadā centra izstrādātais projekts «Kolkas lībiešu centra pilnveidošana» saņēma finansiālo atbalstu no Nīderlandes Karalienes Julianas fonda. Projekta tapšanā un īstenošanā lielas pūles ieguldīja fonda koordinatore Latvijā&nbsp;<strong>Ieva Ernštreite</strong>. Līvu centrs kļuva par mūsdienīgi aprīkotu vietu, kur lībieši varēja ikdienā pulcēties, stiprināt savu lībisko identitāti, apgūstot jaunas zināšanas un iemaņas, kā arī par vietu, kur popularizēt lībiešu kultūrvēsturisko mantojumu daudzajiem Kolkas viesiem.</p>
                                                            <p>Kopš Latvija 2004. gadā iestājās Eiropas Savienībā, ievērojami palielinājās ārzemju tūristu plūsma. Uzskatāmi to var redzēt, pāršķirstot Kolkas lībiešu centra apmeklējumu grāmatu. Ja pirms tam starp pašmāju interesentu ierakstiem pavīdēja pa kādam ārzemnieka ierakstam galvenokārt tepat no radu tautu zemēm — Igaunijas un Somijas, tad tagad ierakstu ģeogrāfija ir būtiski paplašinājusies. Līvu centru Kūolka šajos gados apmeklējuši tūristi no Anglijas, <span class="caps">ASV</span>, Austrālijas, Austrijas, Beļģijas, Brazīlijas, Čehijas, Dānijas, Francijas, Igaunijas, Itālijas, Kanādas, Krievijas, Nīderlandes, Polijas, Somijas, Spānijas, Šveices, Ukrainas, Vācijas, Zviedrijas. Arī apmeklētāju skaits gadā audzis no apmēram 300 viesiem 2001. gadā līdz vairāk nekā 1000 interesentiem 2010.&nbsp;gadā.</p>
                                                            <hr>
                                                            <p><strong>Līvu informācijas centrā Kūolka</strong><br>ir pieejama plaša informācija par lībiešiem — te var iepazīties ar dažādiem izdevumiem lībiešu un latviešu valodā, var noskatīties filmas video un <span class="caps">DVD</span> formātā, noklausīties lībiešu teicēju, lībiešu dziesmu un lībiešu valodas paraugu ierakstus. Skatāmi lībiešu sadzīves priekšmeti, zvejniecības un zivju apstrādes rīki, rokdarbi. Izveidoti foto stendi par ciema vēsturi “Kolkas ciems un tā pamatiedzīvotāji — lībieši senāk un&nbsp;tagad”.</p>
                                                            <p><img alt="Mārīte Zandberga tūristiem Līvu centrā stāsta par lībiešu tautastērpu" class="alignleft" src="../../content/centrs.jpg" style="margin-left: 8px; margin-right: 8px; margin-top: 8px; margin-bottom: 8px; float: left; width: 300px; height: 212px; " title="Mārīte Zandberga tūristiem Līvu centrā stāsta par lībiešu tautastērpu"></p>
                                                            <p>Paplašinātās telpas deva iespēju Kolkas lībiešiem aizvadītajos gados uzņemt pie sevis dažādas izstādes. Ļoti cieši ar lībiešu dzīves veidu un kultūru bija saistīta Somijas Muzeju pārvaldes un Somijas vēstniecības Latvijā kopīgi veidotā izstāde «Lībiešu tautas kultūra. Fotogrāfijas no Kurzemes lībiešu ciemiem. 1901.–1927.» Japāņu izcelsmes zviedru fotomākslinieka&nbsp;<strong>Juki Nakamuras</strong>&nbsp;fotoizstāde «Fotostāsts par lībiešiem Kurzemes krastā» vēstīja par lībiešiem mūsdienās. Brazīlijas Latviešu draugu fonda sagatavotā fotoizstāde «Latvieši Brazīlijā» ļāva ieskatīties mūsu tautiešu dzīvē un atcerēties, ka bija ne mazums lībiešu ciemu iedzīvotāju, kuri savulaik emigrēja uz Brazīliju. Laikraksta “Talsu Vēstis” fotogrāfa&nbsp;<strong>Daiņa Kārkluvalka</strong>&nbsp;izstāde «Svētku mirkļi» stāstīja par Lībiešu svētkiem Mazirbē 2005.&nbsp;gadā.</p>
                                                            <p>Līvu centrā var iepazīties ar lībiešu gleznotāja&nbsp;<strong>Jāņa Beltes</strong>&nbsp;un viņa meitas&nbsp;<strong>Zelmas Beltes&nbsp;</strong>gleznām. Te notikušas lībiešu izcelsmes mākslinieka&nbsp;<strong>Andreja Šulca</strong>&nbsp;un Mazirbes skolotāja<strong>Alvila Krūzes</strong>&nbsp;gleznu izstādes.</p>
                                                            <p>Kolkas grupas locekle&nbsp;<strong>Aina Bolšinga</strong>, pati būdama čakla rokdarbniece, ir noorganizējusi vairākas rokdarbu izstādes. Īpaši interesanta bija ciema rokdarbnieču izstāde «Lībiešu zeķes». Jaunākās paaudzes pārstāve&nbsp;<strong>Marika Baļķīte</strong>&nbsp;izveidoja personālizstādi &lt;Lībiešu raksti un krāsas». Savu prasmi viņa mācīja arī kolceniecēm. Darinājumus varēja apskatīt izstādē. Centra apmeklētājus ir priecējuši arī Kolkas kokamatnieka&nbsp;<strong>Viestura Ķierpes</strong>kokizstrādājumi.</p>
                                                            <hr>
                                                            <p>Līvu centra iekārtošana un dažādu ar lībiešiem saistītu pasākumu organizēšana bija Kolkas grupas darbības viena puse. Taču galvenais uzdevums bija mācīties lībiešu valodu. No 1991. gada valodas nodarbības notika Kolkas skolā, un tās vairākus gadus vadīja&nbsp;<strong>Viktors un Marta Bertholdi</strong>. Iesākumā mācīties gribētāju bija gandrīz 20 cilvēku, bet vēlāk to skaits nostabilizējās līdz desmit. Lībiešu valodas zināšanas centās atsvaidzināt un uzturēt Kolkas lībiešu dzimtu pēcnācēji. Mācības lībiešu valodas lielisko zinātāju vadībā beidzās 1994. gadā, jo decembrī Marta Bertholde aizgāja mūžībā, bet Viktors 1995. gadā pārcēlās uz dzīvi Liepājā. Pēc tam valodas nodarbības ir turpinājušās, bet laika gaitā mācīties gribētāju skaits ir sarucis.</p>
                                                            <p>Ne mazums pūļu Kolkas lībieši ieguldījuši, vācot senas fotogrāfijas, atmiņas un rakstītās liecības par savu dzimto pusi. Daļa no iegūtajiem materiāliem ir apskatāmi Līvu centrā. Grupas dalībnieki pētījuši senās mājvietas, ne reizi vien ielūkojušies citos lībiešu jūrmalciemos, braukuši iepazīties ar lībiešu novadiem Vidzemē, Kurzemē, Pierīgā. Bijuši visu ar lībiešiem saistīto sarīkojumu dalībnieki Rīgā, Mazirbē, Ventspilī un citur. Kopā svinējuši grupas un tās biedru jubilejas, Mārtiņdienu, Ziemassvētkus.</p>
                                                            <p>Liela daļa no tiem Kolkas lībiešiem, kuri entuziasma pilni iesāka savas grupas un centra veidošanu, ir jau Aizsaulē. Kaut gan grupā skaitās apmēram 60 biedru, tie visi ir cilvēki gados, un aktīvie darītāji ir tikai&nbsp;daži.</p>
                                                            <hr>
                                                            <h3>Lībiešu interešu grupa Kolkas skolā</h3>
                                                            <p><img alt="Lībiešu bērnu un jauniešu vasaras nometnes pasākums notiek Kolkas Ūšu..." class="alignleft" src="../../content/nometne.jpg" style="margin-left: 8px; margin-right: 8px; margin-top: 8px; margin-bottom: 8px; float: right; width: 200px; height: 296px; " title="Lībiešu bērnu un jauniešu vasaras nometnes pasākums notiek Kolkas Ūšu...">Pagājis vairāk nekā 60 gadu kopš Kolkas skolā Mārtiņš Lepste bērniem mācīja lībiešu valodu. Padomju okupācijas gados lībiešu valoda vēl skanēja daļā Kolkas ģimeņu, bet tās runātāji pamazām vien aizgāja aizsaulē. Padomju skolā neko par lībiešiem nemācīja.</p>
                                                            <p>Nāca jauni laiki un lībieši atkal «atgriezās». Kolkā izveidojās Līvu savienības Kolkas grupa. Lībieši un viņu pēcteči bija entuziasma pilni un mēģināja atdzīvināt dzimtās valodas zināšanas. Valodas mācību grupā iesaistījās arī skolotāja Mārīte Zandberga. Radās vēlme izpētīt Kolkas dzimtas un noskaidrot, kuri Kolkas skolēni nākuši no lībiešu ģimenēm, kas agrāk noticis lībiešu jūrmalā, kā dzīvojuši, ko darījuši mūsu senči, kāds folkloras mantojums vēl saglabājies. Pamazām skolotāja nodarbībās iesaistīja arī savus skolēnus. Ja sākumā lībiešu dzīvesveida izpēte galvenokārt balstījās uz spēles un rotaļu elementiem, tad nopietnāku pamatu šis darbs ieguva skolas direktores Zojas Sīles laikā. 1995./1996.&nbsp;mācību gadā Z. Sīle lībiešu valodu mācīja jau kā fakultatīvu priekšmetu. Pēc direktores aiziešanas darbu lībiešu mantojuma apguvē turpināja Mārīte Zandberga, izveidojot Kolkas skolā lībiešu interešu grupu.&nbsp;<img alt="Kolkas skolas lībiešu interešu grupa 2006. gada septembrī piedalās TVraidījumā „Valodo novados”, ko ieraksta Kolkā. To vada profesore Janīna..." class="alignright" src="content/piedalas_raidijuma.jpg" style="margin-left: 8px; margin-right: 8px; margin-top: 8px; margin-bottom: 8px; float: left; width: 300px; height: 201px; " title="Kolkas skolas lībiešu interešu grupa 2006. gada septembrī piedalās TVraidījumā „Valodo novados”, ko ieraksta Kolkā. To vada profesore Janīna...">Skolotāja bērniem deva pamatzināšanas par lībiešu kultūrvēsturi, cilvēkiem, tradicionālo nodarbošanos, ieinteresēja bērnus meklēt savas lībiskās saknes. Bērni apguva lībiešu valodas pamatus. Protams, ir atšķirība starp mūsdienām un Latvijas brīvvalsts laikiem: toreiz bērni mācījās valodu, tagad galvenais uzdevums ir stiprināt lībiskās identitātes apzināšanos.</p>
