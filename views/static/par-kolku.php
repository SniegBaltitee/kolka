                                                            <h3>Domesnes un Kolka</h3>
                                                            <p><strong>Kolkasraga</strong> senākais nosaukums Domesnes pirmoreiz lasāms Zviedrijā atrastajā Mervalas rūnakmenī. To datē ar 1040. gadu. Rūnakmenī Kolkasrags apzīmēts ar vārdu Tumisnis, fonētiskā rakstībā tas būtu Domisnaes. Valodniece Benita Laumane grāmatā «Zeme, jūra, zvejvietas: Zvejniecības leksika Latvijas piekrastē» devusi šī vietvārda izcelsmes un nozīmes skaidrojumu. Nosaukuma otrā daļa «nes» varētu būt skandināvu cilmes sugas vārda zemesrags apzīmēšanai. Vietvārda pirmā daļa «Domes» varētu būt cēlusies no latīņu domus ecclesiae, kas vāciskā nosaukumā skanētu «Gemeindehaus mit Wohnung der Geistlichkeit»(Draudzes nams ar mājvietu garīdzniecībai).</p>
															<p>Viduslaiku valodu speciāliste profesore Bjanka Patoki (Bianca Patocchi) no Šveices personiskā sarakstē devusi nedaudz atšķirīgu domus ecclesiae skaidrojumu. Minētais jēdziens saistoties ar reliģisku rituālu vietu, nozīmējot — sapulču nams, kulta nams, bet ne baznīcu kā ēku, kur notiek liturģiskās ceremonijas. Domus ecclesiae apzīmējot privātu namu, vietu, kuru kulta vajadzībām lietojuši kristieši mūsu ēras pirmajos gadsimtos. Pieņemamāks šķiet otrais skaidrojums, jo Kolkasraga senais nosaukums Domesnes ir veidojies laikā, kad kristietība vēl nebija ienākusi mūsu zemē. Tomēr abi skaidrojumi nav pretrunā, ļaujot pieņemt, ka nosaukuma Domesnes pamatā varētu būt atsauce uz kādu reliģiska rakstura ēku.</p>
                                                                <hr>
                                                            <p>Ciema kā apdzīvotas vietas nosaukums pirmo reizi atrodams Lībekas domkunga Varendorfa tiesas spriedumā, kas datēts ar 1387. gada 17. oktobri. Dokumentā nosaukti apmēram 20 ciemi, kas piederējuši Rīgas domkapitula valdījumā esošai Dundagas pilij, viens no tiem saucies<strong> Domesnes</strong>.</p>
                                                            <p>Dokumentos nosaukums <strong>Kolka</strong> sastopams kopš 18. gs. Tā 1741. gada 9. marta ieraksts Irbes–Ģipkas draudzes grāmatā liecina, ka kristīts «Behrtuls, Krohga Didrika dels no Kolkes zeema». Arī turpmākajos ierakstos mācītāji lietojuši Kolkas ciema vai vienkārši Kolkas vārdu. No kura laika abi pašlaik lietotie vietvārdi Kolka un Kolkasrags faktiski ienākuši sadzīvē, var izteikt tikai pieņēmumus. Kolkasraga apzīmējums kartēs un citos oficiālos dokumentos līdz 19. gadsimta beigām ir Domesnes, bet vietējie iedzīvotāji ciemu saukuši par Kolkasragu. Tikai 20. gadsimtā nostiprinājies apdzīvotās vietas nosaukums Kolka.</p>
                                                            <p>Nosaukums Kolka, lībiski Kūolka, saistāms ar Baltijas jūras somu valodās izplatītu vārdu: tas pielīdzināms igauņu kolgas, kolk, somu kolkka — kakts, stūris, nostūris, igauņu valodā sastopama arī nozīme «jūras līcis». Tomēr lībiešu valodā šis vārds zināms tikai kā vietvārds, tādēļ pieļaujams, ka «Kolka» ir igauņu izcelsmes nosaukums. Bet lībiešu lietotais Kolkasraga apzīmējums Kūolka nana burtiski tulkojams kā «Kolkas deguns».</p>
                                                            <p>Tādi ir vietvārdu Kolka un Domesnes izcelsmes un nozīmes skaidrojumi, kas balstīti uz senākiem rakstītiem avotiem. Taču bieži rakstos, īpaši jau tūrisma literatūrā, sastopams cits nosaukuma skaidrojums, kas šķiet itin nopietns un balstīts kādā senā leģendā. Teika «Kā cēlies Kolkas nosaukums» publicēta arī Pirmajā lībiešu lasāmgrāmatā (Ežmi līvõd lugdõbrōntõz, Tartu, 1921.) košradznieka Augusta Skadiņa stāstījumā:</p>
                                                            <p style="color: green">Senatnē lībieši karoja ar vāciešiem. Viens lībietis dzina septiņus vāciešus no lielā purva līdz Kolkas ragam, kur bija pēdējā cīņa. Lībietis ar koka vāli krāva vācietim pa galvu, saukdams: “Kūol ka, rakkarz, sa tän jära! Nomirsti arī, rakari, šeit!” Tā tas “kūol-ka” vārds stāv vēl šodien.”</p>
                                                            <p>«Dzimtenes Vēstneša» 1914. gada 11. (24.) oktobrī publicētajā rakstā «Kaut kas par tagadējiem lībiešiem» atrodama teika par vārda Domesnes izcelšanos, ko raksta autoram (diemžēl publikācija ir bez paraksta) stāstījis «kāds vecs lībietis»:</p>
                                                            <p style="color: green">Tanī vietā, kur tagad ir Rīga, agrāk bijis lībiešu ciems ar vārdu “Dom” (vēlāk tur uzbūvēta Doma baznīca). Kad vācieši ieradās Daugavas grīvā, viņi aizdzina Doma iedzīvotājus, kuri meklēja patvērumu tagadējā Kolkasragā. Vēlāk vācieši viņus te atrada un nāca iekarot šo «Domes nest» (Doma ligzda).</p>
                                                            <p>Protams, šī ir tikai tautas etimoloģija un tāpat nebūtu pieskaitāma vērā ņemamiem vietvārda skaidrojumiem. Bet interesanta no tāda viedokļa, kā vietējie iedzīvotāji centušies skaidrot savas dzīvesvietas nosaukuma izcelsmi.</p>
